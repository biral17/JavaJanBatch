public class Secondmain {

    public static void main(String[] args){
        //conditions
        //functions
        //operators = , -, <<, >> ! ||

        int x;  //declaration

         x=10;
        int y = 20; //initialization
         int z;

         //swap process
         z = x;
         x = y;
         y = z;

         //after swapping x=20, y=10
        System.out.println("swap process...");
        System.out.println("value of x = " + x);
        System.out.println("value of y = " + y);



    }
}
